function setLanguage(lang) {
    localStorage.setItem('ku_lang', lang);
    document.documentElement.lang = lang;

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n').split('.');
        let translation = translations[lang];
        key.forEach(k => {
            translation = translation ? translation[k] : null;
        });

        if (translation) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                el.innerHTML = translation;
            }
        }
    });

    // Handle specific options for select
    document.querySelectorAll('option[data-i18n-type]').forEach(opt => {
        const type = opt.getAttribute('data-i18n-type');
        if (translations[lang].contact.types[type]) {
            opt.textContent = translations[lang].contact.types[type];
        }
    });

    // Update active state of toggle
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.id === `lang-${lang}`);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('ku_lang') || (navigator.language.startsWith('es') ? 'es' : 'en');
    setLanguage(savedLang);

    document.getElementById('lang-es')?.addEventListener('click', () => setLanguage('es'));
    document.getElementById('lang-en')?.addEventListener('click', () => setLanguage('en'));
});
